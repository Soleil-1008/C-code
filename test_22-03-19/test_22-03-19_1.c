#define _CRT_SECURE_NO_WARNINGS 1
//单目操作符
#include<stdio.h>
int main()
{
	//sizeof
	short s = 0;
	int a = 10;
	printf("%d\n", sizeof(s = a + 5));//2  由s决定  
	printf("%d\n", s);//0   s并未计算

	int m = 0;
	//~按(2进制)位取反
	//00000000000000000000000000000000-补码
	//11111111111111111111111111111111-补码
	//11111111111111111111111111111110-反码
	//10000000000000000000000000000001-原码
	//-1
	printf("%d\n", ~m);//打印的是原码  -1

	int n = 11;
	n = n | (1 << 2);
	printf("%d\n", n);//15
	n = n & (~(1 << 2));
	printf("%d\n", n);//11
	//00000000000000000000000000001011   第三位变成1
	//00000000000000000000000000000100   由1<<2产生  按位或
	//00000000000000000000000000001111   第三位变成0
	//11111111111111111111111111111011   1<<2再取反  按位与
	//00000000000000000000000000001011
	return 0;
}