#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
int i;//全局变量-不初始化，默认为0   局部变量-不初始化，随机值
int main()
{
	i--;
	//-1
	//10000000000000000000000000000001-原码
	//11111111111111111111111111111110-反码
	//11111111111111111111111111111111-补码->无符号数（本身）
	if (i > sizeof(i))//sizeof()-计算类型所占内存大小 恒>=0（无符号数）先将i变成一个无符号数
	{
		printf(">\n");//>
	}
	else
	{
		printf("<\n");
	}
	return 0;
}